package sample;

public class TestCase {
	
	private String testName;
	
	private String inputFilePath;
	
	private String xmlssLoadConf;
	
	private String xmlssReportConf;
	
	private String outputFilePath;
	
	private int mode;

	public String getTestName() {
		return testName;
	}

	public void setTestName(String testName) {
		this.testName = testName;
	}

	public String getInputFilePath() {
		return inputFilePath;
	}

	public void setInputFilePath(String inputFilePath) {
		this.inputFilePath = inputFilePath;
	}

	public String getXmlssLoadConf() {
		return xmlssLoadConf;
	}

	public void setXmlssLoadConf(String xmlssLoadConf) {
		this.xmlssLoadConf = xmlssLoadConf;
	}

	public String getXmlssReportConf() {
		return xmlssReportConf;
	}

	public void setXmlssReportConf(String xmlssReportConf) {
		this.xmlssReportConf = xmlssReportConf;
	}

	public int getMode() {
		return mode;
	}

	public void setMode(int mode) {
		this.mode = mode;
	}

	public String getOutputFilePath() {
		return outputFilePath;
	}

	public void setOutputFilePath(String outputFilePath) {
		this.outputFilePath = outputFilePath;
	}
	
}
